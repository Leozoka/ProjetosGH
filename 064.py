marca = 'asus'
if marca == 'samsung':
    print('True')
else:
    print('False')

# Verificou-se a variável, usando o duplo igual (==), caso este operador retorne True, os valores no lado esquerdo e direito do operador são iguais.
# Se não forem iguais eles nos devolve dizendo que é False. (Como acabou de acontecer no exemplo acima).
