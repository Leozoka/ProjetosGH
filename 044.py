números = [7, 8, 9, 2, 6, 0, 87, 100]
minimo = min(números)
maximo = max(números)
soma = sum(números)
print('Número mínimo: {}\nNúmero máximo: {}\nSoma: {}'.format(minimo, maximo, soma))

